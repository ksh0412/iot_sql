package cal;

public class Cal {
	public int numPlus(int a, int b){
		int result = a+b;
		return result;
	}
	
	public int numMinus(int a, int b){
		int result = a-b;
		return result;
	}
	
	public int numMultiple(int a, int b){
		int result = a*b;
		return result;
	}
	
	public int numDivision(int a, int b){
		int result = a / b;
		return result;
	}
}
